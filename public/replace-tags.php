function slugify($t) {
    $s = preg_replace('/\s+/', '-', trim($t));
    $s = preg_replace('/[^\x{0600}-\x{06FF}a-zA-Z0-9\-_]/u', '', $s);
    $s = trim($s, '-');
    return mb_strtolower($s, 'UTF-8');
}

